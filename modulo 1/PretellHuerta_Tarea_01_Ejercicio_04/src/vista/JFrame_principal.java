    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
      String nombre = txt_nombre.getText();
      int codigo =  Integer.parseInt(txt_codigo.getText());
      String carrera = txt_carrera.getText();
      int dni = Integer.parseInt(txt_dni.getText());
      int edad = Integer.parseInt(txt_edad.getText());
      
      EstudiantesUniversitarios dato1 = new EstudiantesUniversitarios(nombre, codigo, carrera, dni, edad);
      objArreglo_EstudiantesUniversitarios.registrar(dato1);
      objArreglo_EstudiantesUniversitarios.listar(tbl_table);
      
        
    }//GEN-LAST:event_btn_registrarActionPerformed

