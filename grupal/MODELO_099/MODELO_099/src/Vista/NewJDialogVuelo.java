    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        try {
            int numero = Integer.parseInt(txtnumero.getText());
            Vuelos encontrado = objarbol.Buscar(objarbol.getRaiz(), numero);

            if (encontrado != null) {
                SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
                SimpleDateFormat formatoHora = new SimpleDateFormat("HH:mm");

                txtfecha.setText(formatoFecha.format(encontrado.getFechaVuelo()));
                txthora.setText(formatoHora.format(encontrado.getHoraVuelo()));
                txtorigen.setText(encontrado.getOrigenVuelo());
                txtdestino.setText(encontrado.getDestinoVuelo());
                txtplazatotal.setText(String.valueOf(encontrado.getPlazasTotales()));
                txtplazaturist.setText(String.valueOf(encontrado.getPlazasTurista()));
            } else {
                JOptionPane.showMessageDialog(this, "Vuelo no encontrado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_consultarActionPerformed

    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        try {
            int numero = Integer.parseInt(txtnumero.getText());
            SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat formatoHora = new SimpleDateFormat("HH:mm");

            Date fechaVuelo = formatoFecha.parse(txtfecha.getText());
            Date horaVuelo = formatoHora.parse(txthora.getText());
            String origen = txtorigen.getText();
            String destino = txtdestino.getText();
            int plazasto = Integer.parseInt(txtplazatotal.getText());
            int plazastu = Integer.parseInt(txtplazaturist.getText());

            Vuelos actualizado = new Vuelos(numero, fechaVuelo, horaVuelo, origen, destino, plazasto, plazastu);

            DAO_Vuelos.Actualizar(actualizado);

            objarbol.setRaiz(objarbol.Eliminar(objarbol.getRaiz(), numero));
            objarbol.setRaiz(objarbol.Insertar(objarbol.getRaiz(), actualizado));

            objarbol.Listar(tbl_vuelos);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al editar: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_editarActionPerformed

