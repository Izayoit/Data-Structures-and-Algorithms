    private void btn_insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertarActionPerformed
        try {

            int numero = Integer.parseInt(txtnumero.getText());
            SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat formatoHora = new SimpleDateFormat("HH:mm");

            String textoFecha = txtfecha.getText();
            String textoHora = txthora.getText();

            // Convertir a Date
            Date fechaVuelo = formatoFecha.parse(textoFecha);
            Date horaVuelo = formatoHora.parse(textoHora);
            String origen = txtorigen.getText();
            String destino = txtdestino.getText();
            int plazasto = Integer.parseInt(txtplazatotal.getText());
            int plazastu = Integer.parseInt(txtplazaturist.getText());

            Vuelos nuevovuelo = new Vuelos(numero, fechaVuelo, horaVuelo, origen, destino, plazasto, plazastu);

            DAO_Vuelos.Insertar(nuevovuelo);

            objarbol.setRaiz(objarbol.Insertar(objarbol.getRaiz(), nuevovuelo));

            objarbol.Listar(tbl_vuelos);

            txtnumero.setText("");
            txtdestino.setText("");
            txtfecha.setText("");
            txthora.setText("");
            txtorigen.setText("");
            txtplazatotal.setText("");
            txtplazaturist.setText("");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_insertarActionPerformed

