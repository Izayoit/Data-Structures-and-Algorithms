    private void btn_registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registroActionPerformed
     int numero = Integer.parseInt(txt_numero.getText());
     numeros datos = new numeros(numero);
     
     Nodo raiz =objArbol.insertar(objArbol.getRaiz(), datos);
     objArbol.setRaiz(raiz);
     objArbol.setRaiz(objArbol.equi(objArbol.getRaiz()));
     objArbol.listar(tbl_datos);
     objArbol.totalizarMayor(txt_mayor);
     objArbol.TotalizarMenor(txt_menor);
     
     
     
     
    }//GEN-LAST:event_btn_registroActionPerformed

