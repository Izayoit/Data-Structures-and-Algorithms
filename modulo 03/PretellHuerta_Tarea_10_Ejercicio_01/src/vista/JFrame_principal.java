    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
    
        String nombre = txt_nombre.getText();
        int codigo = Integer.parseInt(txt_codigo.getText());
        String descrip = txt_descrip.getText();
        
       producto dato1 = new producto(codigo, nombre, descrip);
       
      Nodo raiz= objArbolBinario.insertar(dato1, objArbolBinario.getRaiz());
      objArbolBinario.setRaiz(raiz);
      objArbolBinario.setRaiz( objArbolBinario.equi(objArbolBinario.getRaiz()));
      objArbolBinario.listar(tbl_datos);
   
       
        
        
    }//GEN-LAST:event_btn_registrarActionPerformed

