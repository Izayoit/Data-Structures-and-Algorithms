    private void btn_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RegistrarActionPerformed
        int Codigo = Integer.parseInt(txt_Codigo.getText());
        String Nombre = txt_Nombre.getText();
        double Prestamo = Double.parseDouble(txt_Prestamo.getText());
        Object[] Registro = new Object[]{Codigo,Nombre,Prestamo};
        Cliente Elemento = new Cliente(Registro);
        
        Nodo Raiz = objArbol.Insertar(objArbol.getRaiz(), Elemento);
        objArbol.setRaiz(Raiz);
        objArbol.setRaiz(objArbol.equi(objArbol.getRaiz()));
        objArbol.Listar(tbl_Datos);
        objArbol.Totalizar(txt_Total);
    }//GEN-LAST:event_btn_RegistrarActionPerformed

